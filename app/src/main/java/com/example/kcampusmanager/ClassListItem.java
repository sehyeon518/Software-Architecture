package com.example.kcampusmanager;

public class ClassListItem {
    private String name;
    private Boolean open;

    public String getName() {
        return this.name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public Boolean getOpen() {
        return this.open;
    }

    public void setOpen(Boolean open) {
        this.open = open;
    }
}
